function GeneratePixelPattern($pattern) {
    $width = count($pattern[0]);
    $height = count($pattern);

    $im = imagecreatetruecolor($width, $height);

    $bgc = imagecolorallocate($im, 255, 255, 255);
    imagefilledrectangle($im, 0, 0, $width, $height, $bgc);

    $pixelColor = imagecolorallocate($im, 0, 0, 0);

    for ($y = 0; $y < $height; $y++) {
        for ($x = 0; $x < $width; $x++) {
            if ($pattern[$y][$x] === 1) {
                imagesetpixel($im, $x, $y, $pixelColor);
            }
        }
    }

    return $im;
}

